class AkHtmlToSmartypants
{
    function convert()
    {
        require_once(AK_VENDOR_DIR.DS.'TextParsers'.DS.'smartypants.php');
        $Smartypants = new SmartyPantsTypographer_Parser();
        return $Smartypants->transform($this->source);
    }
}

